def area(a, h): 
    # Принимает числа a и h - сторону и высоту правильного прямоугольника, возвращает площадь правильного триугольника 
    return a * h / 2 

def perimeter(a, b, c): 
    # Принимает числа a и h - сторону и высоту правильного прямоугольника, возвращает периметр правильного триугольника 
    return a + b + c 
